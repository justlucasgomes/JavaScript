//Em JavaScript, as estruturas de controle permitem que você direcione o fluxo de execução do código com base em condições e repita blocos de instruções conforme necessário. Aqui estão os principais tipos:

//Coondicionais:
    //if/else/else if: Permite executar blocos de código se uma condição for verdadeira ou, alternativamente, outro bloco se a condição for falsa.

