//​Em JavaScript, os operadores relacionais são utilizados para comparar valores, retornando um valor booleano (true ou false) conforme a veracidade da comparação. Os principais operadores relacionais incluem:​

//  Maior que (>): Verifica se o valor à esquerda é maior que o valor à direita.​
  console.log(10 > 5); // true

//  Menor que (<): Verifica se o valor à esquerda é menor que o valor à direita.​
  console.log(3 < 7); // true

//  Maior ou igual a (>=): Verifica se o valor à esquerda é maior ou igual ao valor à direita.​
  console.log(4 >= 4); // true

//  Menor ou igual a (<=): Verifica se o valor à esquerda é menor ou igual ao valor à direita.​
  console.log(6 <= 5); // false

//  Igual a (==): Compara se os valores são iguais, realizando conversão de tipo se necessário.​
  console.log('5' == 5); // true

//  Estritamente igual a (===): Compara se os valores e os tipos são iguais, sem realizar conversão de tipo.​
  console.log('5' === 5); // false

//  Diferente de (!=): Verifica se os valores são diferentes, realizando conversão de tipo se necessário.
  console.log(10 != '10'); // false

//  Estritamente diferente de (!==): Verifica se os valores e os tipos são diferentes, sem realizar conversão de tipo.​
  console.log(10 !== '10'); // true

//  Esses operadores são fundamentais para controlar o fluxo de execução em estruturas condicionais, como instruções if e loops, permitindo a tomada de decisões com base em comparações entre valores.