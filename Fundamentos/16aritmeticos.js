//​Em JavaScript, os operadores aritméticos são utilizados para realizar operações matemáticas básicas. Os principais operadores incluem:​

//Adição (+): Soma dois valores.​
  let resultado = 5 + 3; // resultado é 8

//Subtração (-): Subtrai o valor à direita do valor à esquerda.​
  let resultado1 = 10 - 4; // resultado é 6

//Multiplicação (*): Multiplica dois valores.​
  let resultado2 = 7 * 2; // resultado é 14

//Divisão (/): Divide o valor à esquerda pelo valor à direita.​
  let resultado3 = 20 / 5; // resultado é 4

//Resto da divisão (%): Retorna o restante da divisão do valor à esquerda pelo valor à direita.​
  let resultado4 = 10 % 3; // resultado é 1

//Incremento (++): Adiciona 1 ao valor da variável.​
  let contador = 0;
  contador++; // contador agora é 1

//Decremento (--): Subtrai 1 do valor da variável.​
  let contador1 = 5;
  contador1--; // contador agora é 4

//Exponenciação (**): Eleva o valor à esquerda à potência do valor à direita.
  let resultado5 = 3 ** 2; // resultado é 9
  
//Esses operadores são fundamentais para manipulações numéricas em JavaScript.