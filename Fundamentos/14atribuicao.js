/*
​Em JavaScript, a atribuição é o processo de armazenar um valor em uma variável, permitindo que o valor seja referenciado e manipulado posteriormente no código. O operador de atribuição básico é o sinal de igual (=), que associa o valor à direita à variável à esquerda.
*/

const a = 7;
let b = 3;

b += a; // b = b + a
console.log(b);

b -= 4; //b = b - 4
console.log(b);

b *= 2; //b = b * 2
console.log(b);

b/=2;
console.log(b);

b %= 2; // b = b % 2
console.log(b);
